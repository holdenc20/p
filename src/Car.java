
public class Car {
	int CarY;
	int CarX;

	public Car(int CarX, int CarY) {
		this.CarY = CarY;
		this.CarX = CarX;
	}

	public void setCarY(int CarY) {
		this.CarY = CarY;
	}

	public void setCarX(int CarX) {
		this.CarX = CarX;
	}

	public int getCarY() {
		return CarY;
	}

	public int getCarX() {
		return CarX;
	}
}
